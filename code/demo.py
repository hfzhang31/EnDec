def main(args): 
    # Initial arg processing and log
    prompt = args.prompt
    args.normalizers = (args.normalizers.split(",") if args.normalizers else [])

    model, tokenizer, device = load_model(args)
    jailbreak = LocalJail(args, tokenizer)

    _, _,  decoded_output_with_watermark, _ = generate(prompt, args, model=model, device=device, tokenizer=tokenizer, jailbreak=jailbreak)
    print("[ATK]:===",decoded_output_with_watermark)
    
    _, _,  decoded_output_without_watermark, _ = generate(prompt, args, model=model, device=device, tokenizer=tokenizer, jailbreak=jailbreak)
    print("[CLR]:===",decoded_output_without_watermark)

    return

